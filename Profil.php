<h2>Présentation du BTS SIO</h2>

<p>
    Le BTS SIO est un diplôme informatique de 2 ans qui se trouve dans a peu près un total de 200 lycées publiques ou privées sous contrat dans le territoire français, d'un taux de réussite varié de 60% à 95%, a la fin de cette formation on peut intervenir en entreprise en que développeur d'applications informatiques ou administrateur de réseaux locaux.
</p>

<h3>Contenu de la formation</h3>



<p>
La formation d’une part est basé sur les compétences informatiques (système et réseau, développement de logiciel, bases de données et cybersécurité), sur les cours entrepreneuriales (culture économique juridique et managériale appliquée à l’informatique) et les enseignements généraux (culture générale et expression, mathématiques pour l’informatique, anglais) appliqués à l’informatique.

A la fin du premier semestre, ont choisi de se spécialiser dans le domaine de l’administration des systèmes et des réseaux (option SISR) ou dans le développement de logiciels (option SLAM).
​
Durant la formation de BTS SIO, chaque étudiant bénéficie de 13 semaines de stage en entreprise, 6 semaines au cours de la première année et 7 au cours de la seconde année. Ces stages sont suivis par un professionnel maître de stage.

</p>

